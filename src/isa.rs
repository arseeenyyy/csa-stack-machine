pub enum Instr {
    Nop,
    Jump(String),
    Call(String),
    BranchZero(String),
    BranchPositive(String),
    Load(String),
    Store(String),
    PushAddr(String),
    PushNum(i32), // same ISA instruction as PushAddr, both compiled to one opcode
    LoadIndirect,
    StoreIndirect,
    Return,
    Add,
    Sub,
    MultiplyLow,
    MultiplyHigh,
    Divide,
    Remainder,
    Not,
    Neg,
    And,
    Xor,
    Dup,
    Drop,
    Out(i32),
    In(i32),
    EnableEam,
    DisableEam,
    EnableInterrupts,
    DisableInterrupts,
    Halt,
}